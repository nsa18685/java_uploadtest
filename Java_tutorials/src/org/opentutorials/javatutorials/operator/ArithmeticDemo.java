package org.opentutorials.javatutorials.operator;

public class ArithmeticDemo {

	public static void main(String[] args) {
		// result의 값은 2
		int result = 1*2;
		System.out.println(result);
		
		//result의 값은 10
		result= result +8;
		System.out.println(result);
		
		//result의 값은 3
		result= result%7;
		System.out.println(result);
				

	}

}
